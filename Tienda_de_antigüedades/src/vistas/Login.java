    public void validar(){
        String id_usuario = txtUsuario.getText();
        String contraseña = txtContraseña.getText();
        if( txtUsuario.getText().equals("") || txtContraseña.getText().equals("") ){
            JOptionPane.showMessageDialog(this, "Debe ingresar datos!");
        }
        else{
            us = udao.ValidarUsuario(id_usuario , contraseña);
            if( us.getId_user() != null && us.getContraseña()!=null) {
                VentaForm ve = new VentaForm();
                ve.setVisible(true);
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Datos ingresados incorrectos!");
            }
        }
    }

